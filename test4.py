# 매개 변수로 두 개의 리스트를 받아, 첫번째 매개변수로 받은 리스트에 두번째 리스트의 데이터를 삭제한 리스트를 리턴하는 함수를 만드시오.
# 단, 첫번째 매개변수로 들어온 리스트가 반드시 두 번째 매개변수로 들어온 리스트보다 데이터의 수가 많다고 가정한다.
# 두 매개변수 : [1, 3, 5, 7, 9], [1, 5] 결과 : [3, 7, 9]

li1 = [1, 3, 5, 7, 9]
li2 = [1, 5]

def list(a, b) :
  for i in a :
    for j in b :
      if i == j :
        a.remove(i)
  return a

print(list(li1, li2))